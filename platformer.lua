local triggerSystem = tiny.system()
triggerSystem.filter = tiny.filter("transform&aabb&mask|trigger|effect")
function triggerSystem:update(dt)
  local world = self.world
  for index=1,#self.entities do
    local ent = self.entities[index]
    -- We only want to check for overlaps if the first
    -- entity is a trigger, so we skip over any that aren't
    if not ent.trigger and not ent.effect then goto continue2 end
    local mask = 0
    if ent.trigger then mask = ent.trigger.mask end
    if ent.effect then mask = ent.effect.mask end

    local entered = false
    local activated = false
    for index2=1,#self.entities do
      -- Don't check for an overlap with self
      if index == index2 then goto continue end
      local other = self.entities[index2]
      if not other.mask or other.mask ~= mask then goto continue end
      local collision = aabb_overlap(get_rect(ent), get_rect(other))
      if collision then
        entered = true
        if other.control and other.control.activate then
          activated = true
        end
      end
      ::continue::
    end

    if ent.trigger then
      if entered then
        if not ent.trigger.triggered then
          if ent.trigger.showPopup then
            ent.trigger.triggered = world:addEntity({
                transform = {
                  x=ent.trigger.showPopup.x,
                  y=ent.trigger.showPopup.y,
                },
                popup = {msg=ent.trigger.showPopup.msg},
            })
          end
        end
      else
        world:removeEntity(ent.trigger.triggered)
        ent.trigger.triggered = nil
      end
    elseif ent.effect then
      if activated and not ent.effect.affected then
        trace('activated!')
        if ent.effect.changeLevel then
          ent.effect.affected = true
          local change = ent.effect.changeLevel
          for j=0,change.h-1 do
            for i=0,change.w-1 do
              local index = i + (j * change.w) + 1
              mset(change.x + i, change.y + j, change.data[index])
            end
          end
        end
      end
    end
    ::continue2::
  end
end

function get_rect(e)
    return {
        x=e.transform.x + e.aabb.x,
        y=e.transform.y + e.aabb.y,
        w=e.aabb.w,
        h=e.aabb.h
    }
end

function aabb_overlap(a,b)
  return
    a.x < b.x + b.w and
    a.x + a.w > b.x and
    a.y < b.y + b.h and
    a.y + a.h > b.y
end

function aabb_distance_to(a,b)
  local dx, dy = 0, 0
  if a.x < b.x then dx = b.x - (a.x + a.w)
  elseif a.x > b.x then dx = a.x - (b.x + b.w)
  end
  if a.y < b.y then dy = b.y - (a.y + a.w)
  elseif a.y > b.y then dy = a.y - (b.y + b.w)
  end
  return dx, dy
end

-- rect should be absolutely positioned
function tilemap_collision(rect)
   local x,y = rect.x, rect.y
   local left = x // 8
   local right = (x + rect.w) // 8
   local top = y // 8
   local bot = (y + rect.h) // 8

   local collision = {}
   local any_collision = false
   for i=left,right do
     for j=top,bot do
       local t = mget(i,j)
       if fget(t,0) then
         any_collision = true
         collision[#collision+1] = {x=i,y=j}
       end
     end
   end
   if any_collision then return collision end
   return nil
end

-- Returns distance to map cell
function tilemap_distance_to(a, m)
  local dx, dy = 0, 0
  local mx, my, mw, mh = m.x * 8, m.y * 8, 8, 8
  if a.x < mx then dx = mx - (a.x + a.w)
  elseif a.x > mx then dx = a.x - (mx + mw)
  end
  if a.y < my then dy = my - (a.y + a.h)
  elseif a.y > my then dy = a.y - (my + mh)
  end
  return dx, dy
end

local physicsSystem = tiny.system()
physicsSystem.filter = tiny.filter(
  "transform&aabb&physics|control"
)
function physicsSystem:update(dt)
  for index=1,#self.entities do
    local e = self.entities[index]
    local on_ground = tilemap_collision({
      x = e.transform.x + e.aabb.x,
      y = e.transform.y + e.aabb.y + 1,
      w = e.aabb.w,
      h = e.aabb.h,
    })
    if not on_ground then
      e.physics.on_ground = false
      e.physics.vel.y = math.min(e.physics.max.y, e.physics.vel.y + e.physics.gravity)
    else
      e.physics.on_ground = true
      e.physics.vel.y = e.physics.vel.y * 0.1
      if math.abs(e.physics.vel.y) < 0.0001 then e.physics.vel.y = 0 end
      e.physics.boost_time = 0
    end

    if e.control then
      if e.control.left then
        e.physics.vel.x = math.max(-e.physics.max.x, e.physics.vel.x - e.physics.accel)
      elseif e.control.right then
        e.physics.vel.x = math.min(e.physics.max.x, e.physics.vel.x + e.physics.accel)
      else e.physics.vel.x = e.physics.vel.x * e.physics.friction
      end
      if e.control.jumpp then
        if on_ground then
          e.physics.vel.y = -e.physics.jump
          e.physics.boost_time = e.physics.boost_max
        end
      end
      if e.control.jump then
        if e.physics.boost_time > 0 then
          e.physics.vel.y = e.physics.vel.y - e.physics.boost
          e.physics.boost_time = e.physics.boost_time - 1
        end
      else
        e.physics.boost_time = 0
      end
    end
    resolveMovement(dt/2,e);
    resolveMovement(dt/2,e);
  end
end
function resolveMovement(dt,e)
    -- vs map
    local xvel, yvel = e.physics.vel.x * dt, e.physics.vel.y * dt
    local deltax = xvel
    local deltay = yvel
    local x = e.transform.x + e.aabb.x
    local y = e.transform.y + e.aabb.y
    local w, h = e.aabb.w, e.aabb.h

    local collided = tilemap_collision({x=x+deltax, y=y, w=w, h=h})
    if collided then
      deltax = 0
      e.physics.vel.x = e.physics.vel.x * -0.1
    end
    collided = tilemap_collision({x=x+deltax,y=y+deltay,w=w,h=h})
    if collided then
      deltay = 0
      e.physics.vel.y = e.physics.vel.y * -0.1
    end
    e.transform.x = e.transform.x + deltax
    e.transform.y = e.transform.y + deltay
end

