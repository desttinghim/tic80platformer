-- Load level into memory
function level:load(world, x, y)
  self.x = x * self.w
  self.y = y * self.h

  local index = x + (y * 8)
  data = self.levels[index]

  for i,v in ipairs(data) do
    trace(i..' '..v.t)
    if v.t == "trigger" then
      world:addEntity({
        transform = {
          x = v.x, y = v.y,
        },
        aabb = {
          x = 0, y = 0, w = v.w, h = v.h,
        },
        trigger = v.trigger
      })
    elseif v.t == "lever" then
      world:addEntity({
          transform = {
            x = v.x * 8, y = v.y * 8,
          },
          aabb = {
            x = 0, y = 0, w = 8, h = 8,
          },
          effect = v.effect
      })
    end
  end
end
function level:assert(map, value, expected, msg)
  -- ensure value == expected, or trace msg
  if value ~= expected then
    trace(map.x..','..map.y..": "..msg)
  end
end
function level:assertExists(map, value, msg)
  if not value then
    trace(map.x..','..map.y..": "..msg)
  end
end
function level:assertTile(map, coord, value, msg)
  local tile = mget(map.x + coord.x, map.y + coord.y)
  level:assert(map, tile, value, '('..coord.x..','..coord.y..') '..msg)
end
function level:define(x,y,data)
  -- there is 8 columns per row in the global map
  local index = x + (y * 8)
  self.levels[index] = data
  local map = {x = x * self.w, y = y * self.h}
  for i,v in ipairs(data) do
    if v.t == "trigger" then
      level:assertExists(map, v.x, "missing trigger x")
      level:assertExists(map, v.y, "missing trigger y")
      level:assertExists(map, v.w, "missing trigger w")
      level:assertExists(map, v.h, "missing trigger h")
      level:assertExists(map, v.trigger, "missing trigger component data")
    elseif v.t == "lever" then
      level:assertTile(map, v, 1, "should be a lever")
      level:assertExists(map, v.effect, "missing effect component data")
    end
  end
end

local drawingSystem = tiny.processingSystem()
drawingSystem.filter = tiny.filter(
  "transform&sprite|transform&popup"
)
function drawingSystem:preProcess(dt)
    cls(0)
    map(level.x,level.y,level.w,level.h,0,0)
end
function drawingSystem:process(e, dt)
    local transparent = 0
    if e.sprite then
      spr(
          e.sprite.i,
          e.transform.x - e.sprite.xoff,
          e.transform.y - e.sprite.yoff,
          transparent,
          e.sprite.scale,
          e.sprite.flip,
          e.sprite.rot,
          e.sprite.w,
          e.sprite.h
      )
    elseif e.popup then
        local color = 1
        local h = 6
        local pad = 2
        local x,y = e.transform.x, e.transform.y - (h // 2)
        -- We need to get the width of the text, but we don't
        -- want the result of the first print to be visible,
        -- so we use clip
        clip(0,0,0,0)
        local w = print(e.popup.msg, 0, 0, 0, false, 1, true)
        clip()
        x = x - (w // 2)
        rect(x-pad, y-pad, w+pad*2, h+pad*2, 0)
        -- rectb(x-pad, y-pad, w+pad*2, h+pad*2, 1)
        line(x-pad, y+h+pad, x+w, y+h+pad, 1)
        print(e.popup.msg, x, y, color, false, 1, true)
    end
end

local debugSystem = tiny.processingSystem()
debugSystem.filter = tiny.filter("transform&aabb")
function debugSystem:process(e, dt)
  local startx = e.transform.x + e.aabb.x
  local endx = e.transform.x + e.aabb.x + e.aabb.w
  local starty = e.transform.y + e.aabb.y
  local endy = e.transform.y + e.aabb.y + e.aabb.h
  for i=startx,endx do
    for j=starty,endy do
      if (i - startx) % 2 == (j - starty) % 2 then
      local debugColor = (self.indices[e]) % 15
      pix(i, j, debugColor + 1)
      end
    end
  end
end

local animSystem = tiny.processingSystem()
animSystem.filter = tiny.filter(
  "sprite&anim|control|physics"
)
function animSystem:process(e, dt)
  local state = nil
  if e.physics then
    if e.physics.on_ground == false then
      if e.physics.boost_time > 0 then state = "jump"
      else state = "fall" end
    end
  end
  if e.control then
    if e.control.left then e.sprite.flip = 1 end
    if e.control.right then e.sprite.flip = 0 end
    if not state and (e.control.left or e.control.right) then
      state = "walk"
    end
  end
  if not state then state = "still" end
  e.anim:play(state)
  local index = e.anim:update(dt)
  if index then e.sprite.i = index end
end

